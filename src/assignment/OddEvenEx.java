package assignment;

public class OddEvenEx {

	public static void main(String[] args) {
		for(int number=1;number<=5;number++)
		{
			if(number%2==0)
			{

				System.out.println("this is an even number :" +number);
			}
			else if(number%2==1)
			{
				System.out.println("this is an odd number  :" +number);
			}
		}

	}

}
