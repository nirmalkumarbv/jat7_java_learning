package basics;

public class RelationalOperators {

	public static void main(String[] args) {
	 int a=10, b=15;
	 System.out.println("a > b is --> "+(a>b));
	 System.out.println("a < b is --> "+(a<b));
	 System.out.println("a <= b is --> "+(a<=b));
	 System.out.println("a >= b is --> "+(a>=b));
	 System.out.println("a == b is --> "+(a==b));
	 System.out.println("a != b is --> "+(a!=b));

	}

}
